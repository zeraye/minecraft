func generateCube() *mesh {
	return &mesh{[]triangle{
		// south
		{p: [3]vec3d{{0, 0, 0}, {0, 4, 0}, {4, 4, 0}}},
		{p: [3]vec3d{{0, 0, 0}, {4, 4, 0}, {4, 0, 0}}},
		// east
		{p: [3]vec3d{{4, 0, 0}, {4, 4, 0}, {4, 4, 4}}},
		{p: [3]vec3d{{4, 0, 0}, {4, 4, 4}, {4, 0, 4}}},
		// north
		{p: [3]vec3d{{4, 0, 4}, {4, 4, 4}, {0, 4, 4}}},
		{p: [3]vec3d{{4, 0, 4}, {0, 4, 4}, {0, 0, 4}}},
		// west
		{p: [3]vec3d{{0, 0, 4}, {0, 4, 4}, {0, 4, 0}}},
		{p: [3]vec3d{{0, 0, 4}, {0, 4, 0}, {0, 0, 0}}},
		// top
		{p: [3]vec3d{{0, 4, 0}, {0, 4, 4}, {4, 4, 4}}},
		{p: [3]vec3d{{0, 4, 0}, {4, 4, 4}, {4, 4, 0}}},
		// bottom
		{p: [3]vec3d{{4, 0, 4}, {0, 0, 4}, {0, 0, 0}}},
		{p: [3]vec3d{{4, 0, 4}, {0, 0, 0}, {4, 0, 0}}},
	}}
}
